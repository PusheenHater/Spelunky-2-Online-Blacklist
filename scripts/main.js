function generateSearchTable(data, showReasons)
{
  let html = '';
  
  html += '<table id="searchTable">';
  
  html += '<thead>';
  if (showReasons)
  {
    html += `
      <tr class="header">
        <th style="width: 20%;">Name</th>
        <th style="width: 10%; text-align: center;">Platform</th>
        <th style="width: 70%;">Reason(s)</th>
      </tr>
    `;
  }
  else
  {
    html += `
      <tr class="header">
        <th style="width: 80%;">Name</th>
        <th style="width: 20%; text-align: center;">Platform</th>
      </tr>
    `;
  }
  html += '</thead>';
  
  html += '<tbody';
  html += generateTableRows(data, showReasons);
  html += '</tbody>';
  
  html += '</table>';
  
  return html;
}

function generateTableRows(data, showReasons)
{
  let tableRowsContentHtml = '';

  for (let i = 0; i < data.length; i++)
  {
    if (showReasons)
    {
      tableRowsContentHtml += `
        <tr>
          <td style="padding-left: 5px;">${data[i].name}</td>
          <td style="text-align: center;"><img src="images/${data[i].platform}.png" width="50" height="50"></td>
          <td style="padding-left: 5px;">${data[i].reasons}</td>
        </tr>
      `;
    }
    else
    {
      tableRowsContentHtml += `
        <tr>
          <td style="padding-left: 5px;">${data[i].name}</td>
          <td style="text-align: center;"><img src="images/${data[i].platform}.png" width="50" height="50"></td>
        </tr>
      `;
    }
  }
  
  return `
    ${tableRowsContentHtml}
  `;
}

document.getElementById('searchInput').onkeyup = function()
{
  filterNames();
};

function filterNames()
{
  const input = document.getElementById('searchInput');
  const filter = input.value.toUpperCase();
  const table = document.getElementById('searchTable');
  const tr = table.getElementsByTagName('tr');
  
  let visibleRowCount = 0;
  for (let i = 0; i < tr.length; i++)
  {
    const td = tr[i].getElementsByTagName('td')[0];
    if (td)
    {
      const textValue = td.textContent || td.innerText;
      if (filter === '' || textValue.toUpperCase().indexOf(filter) === 0)
      {
        tr[i].style.display = '';
        
        if (visibleRowCount % 2 === 0)
        {
          tr[i].style.background = '#FAFAFA';
        }
        else
        {
          tr[i].style.background = '#F1F1F1';
        }
        ++visibleRowCount;
      }
      else
      {
        tr[i].style.display = 'none';
      }
    }
  }
}

filterNames();
